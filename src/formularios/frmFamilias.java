 void CargarFamilias(){
   
    String[] titulos = {"ID","Codigo","Descripcion"};
    String[] registro = new String[3];
    modelo= new DefaultTableModel(null,titulos);
    
    BaseDatos.Operacion Operaciones= new BaseDatos.Operacion(); 
    List LisataFamilias= Operaciones.ObtenerFamilias();
   
    Iterator iter = LisataFamilias.iterator();
    while (iter.hasNext())
    {
      Familias familias = (Familias) iter.next();    
      registro[0]=String.valueOf(familias.getIdfamilias());
      registro[1]=familias.getCodigo();
      registro[2]=familias.getDescripcion();
      modelo.addRow(registro);
    }
    tblfamilias.setModel(modelo);
}  
 void BuscarFamiliasID(String vID){
  
  BaseDatos.Operacion Operaciones= new BaseDatos.Operacion(); 
    List LisataFamilias= Operaciones.ObtenerFamiliasID(vID);
   
    Iterator iter = LisataFamilias.iterator();
    while (iter.hasNext())
    {
      Familias familias = (Familias) iter.next(); 
      txtCodigo.setText(familias.getCodigo());
      txtDescripcion.setText(familias.getDescripcion());
      Id_Actualizar=familias.getIdfamilias();
    }
       
  
}

    
